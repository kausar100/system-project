package com.example.hp.tutorstudentportalapp;

public class FetchCtMark {
    private String roll;
    private String mark;

    public FetchCtMark() {
    }

    public FetchCtMark(String roll, String mark) {
        this.roll = roll;
        this.mark = mark;
    }

    public String getRoll() {
        return roll;
    }

    public String getMark() {
        return mark;
    }
}
